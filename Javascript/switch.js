
// Switch in JavaScript
let day = 10;
switch (day) {
    case 1:
        console.log("Monday");
        break; // We use break to exit the switch statement after a case is matched
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
        break;
    case 7:
        console.log("Sunday");
        break;
}